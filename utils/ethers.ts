export const mint = async (quantity: number) => {
    const signer = await connectToMetamask();
	// @ts-ignore
	const account = signer.provider.provider.selectedAddress;
    const contract = new ethers.Contract(contractAddress, contractABI, signer)
    const costToMint = await contract.costToMint()
    const cost = costToMint;
	console.log(ethers.utils.formatUnits(cost, "ether"))
	const value = Number(cost) * quantity
    const options = {
		value: value.toString(),
		gasLimit: 300000,
	};
	const tx = contract.connect(signer);
    const mint = await tx.mint(
	account,
	quantity
	,options
	)
    return mint
}

